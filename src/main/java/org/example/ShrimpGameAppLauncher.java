package org.example;

/**
 * A launcher class for the ShrimpGameApp JavaFX application.
 */
public class ShrimpGameAppLauncher {
  /**
   * The main method that launches the ShrimpGameApp application by calling its main method.
   *
   * @param args the command line arguments passed to the application
   */
  public static void main(String[] args) {
    ShrimpGameApp.main(args);
  }
}